def diagonalDifference(arr):
    # Write your code here
    s = len(arr)
    psum = 0
    ssum = 0
    
    for i in range (0,s):
        psum += arr[i][i]
        ssum +=arr[i][s-1-i]
        
    return abs(psum - ssum)

